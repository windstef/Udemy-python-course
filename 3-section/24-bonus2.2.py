# Cette fois, je veux utiliser la boucle du monde pour produire une série de chiffres en sortie.
# Je veux donc produire des chiffres de un, deux, trois, quatre, cinq, six.
# Ainsi, un, deux, trois, quatre, cinq, six seront imprimés dans la ligne de commande.


x = 1

while x <= 6:
    print(x)
    x = x + 1

print(" Un, deux, trois, quatre, cinq, six.")
print("La fin!")